
package org.sibsutis.is.database.all;

public class AbstractPoint
{
    
    private double X;
    private double Y;
    private double Z;

    public double getZ()
    {
        return Z;
    }

    public void setZ(double Z)
    {
        this.Z = Z;
    }


    public double getY()
    {
        return Y;
    }

    public void setY(double Y)
    {
        this.Y = Y;
    }


    public double getX()
    {
        return X;
    }

    public void setX(double X)
    {
        this.X = X;
    }
    
}
